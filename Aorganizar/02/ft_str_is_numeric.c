int	ft_str_is_numeric(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c >= 48 && c <= 57)
		{
			i++;
		}
		else
		{
			if (c == '\0' || c == 32)
				return (1);
		}
		return (c);
	
	}
	return (1);
}
