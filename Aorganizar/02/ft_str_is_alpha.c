int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		char c;
		
		c = str[i];
		if ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 97 && str[i] <= 122))
		{
			i++;
		}
		else if (str[i] == '\0' || str[i] == 32)
		{
			return (1);
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
